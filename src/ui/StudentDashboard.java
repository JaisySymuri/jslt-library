    private void loadStudentProfile(String studentId) {
        System.out.println("Student ID: " + studentId);
        try {
            Student student = StudentDAO.getStudentById(studentId);
            if (student != null) {
                jTextField1.setText(String.valueOf(student.getStudentId()));
                jTextField2.setText(student.getName());
                jTextField3.setText(student.getEmail()); // Editable
                jTextField4.setText(student.getMajor());
                jTextField5.setText(String.valueOf(student.getEnrollmentYear()));
            } else {
                JOptionPane.showMessageDialog(this, "Student not found.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to load student data.");
        }
    }

