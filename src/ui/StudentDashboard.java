    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        ChangePasswordDTO request = new ChangePasswordDTO(
                studentId,
                getOldPasswordInput(),
                getNewPasswordInput(),
                getConfirmPasswordInput()
        );

        ValidationResult result = ChangePasswordController.process(request);
        if (result.isValid()) {
            JOptionPane.showMessageDialog(this, result.getMessage(), "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, result.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        UpdateEmailDTO dto = new UpdateEmailDTO(studentId, getEmailInput());

        try {
            boolean success = StudentDAO.updateEmail(dto);
            if (success) {
                JOptionPane.showMessageDialog(this, "Email updated successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update email.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

